    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) airlinersTable.getModel();
        dtm.setRowCount(0);

        for (AirlinerDetails a : airDir.getAirliner()) {
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a;
            row[1] = a.getAddress();
            dtm.addRow(row);
        }
    }

