    private void viewAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAirlinerBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = airlinersTable.getSelectedRow();
        if (selectedRow >= 0) {
            AirlinerDetails air = (AirlinerDetails) airlinersTable.getValueAt(selectedRow, 0);
            ViewAirlinerJPanel viewPanel = new ViewAirlinerJPanel(cardSequencePanel, air, airDir);
            cardSequencePanel.add(viewPanel);
            CardLayout layout = (CardLayout) cardSequencePanel.getLayout();
            layout.next(cardSequencePanel);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_viewAirlinerBtnActionPerformed

